    private void tbComidasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbComidasMouseClicked
    
        try {
            PreparedStatement consulta;
            consulta = HomeController.context.conn.prepareStatement("SELECT * FROM comidas WHERE id = ?");
            consulta.setInt(1, Integer.parseInt(tbComidas.getValueAt(tbComidas.getSelectedRow(), 0) + ""));
            
            ResultSet resultado = consulta.executeQuery();
            while(resultado.next()){
                spIdComida.setValue(resultado.getString("id"));
                txtNombreComida.setText(resultado.getString("nombre"));
                txtTipoComida.setText(resultado.getString("tipo"));
                spValor.setValue(resultado.getString("valor"));
                spCantidadComida.setValue(resultado.getString("cantDisponible"));
            }
        } catch(SQLException ex) {
            
        }
        
        cargarTabla();

    }//GEN-LAST:event_tbComidasMouseClicked

