    private void btnGuardarComidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarComidaActionPerformed
        try {
            PreparedStatement consulta;
            
            consulta = HomeController.context.conn.prepareStatement("INSERT INTO comidas (nombre, tipo, valor, cantDisponible) VALUES (?, ?, ?, ?)");
            consulta.setString(1, txtNombreComida.getText());
            consulta.setString(2, txtTipoComida.getText());
            consulta.setFloat(3, Float.parseFloat(spValor.getValue() + ""));
            consulta.setInt(4, Integer.parseInt(spCantidadComida.getValue() + ""));
            
            consulta.executeUpdate();
            System.out.println("Registro guardado correctamente");
        } catch (SQLException ex) {
            System.out.println(ex);            
        }
    }//GEN-LAST:event_btnGuardarComidaActionPerformed

    private void cargarTabla(){
        
        ArrayList<Comidas> datos = new ArrayList<>();
        
        try {
            PreparedStatement consulta;
            consulta = HomeController.context.conn.prepareStatement("SELECT * FROM comidas");
            
            ResultSet resultado = consulta.executeQuery();
            System.out.println("Consulta existosa");
            while(resultado.next()) {
                datos.add(new Comidas(Integer.parseInt(resultado.getString("id")), resultado.getString("nombre"), resultado.getString("tipo"), Float.parseFloat(resultado.getString("valor")), Integer.parseInt(resultado.getString("cantDisponible"))));
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        
        model = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        model.setRowCount(0);
        
        String columNames[] = {"Id", "Nombre", "Tipo", "Valor"};
        model.setColumnIdentifiers(columNames);
        tbComidas.setModel(model);
        
        for(Comidas comida : datos) {
            model.addRow(new Object[]{
                comida.getId(),
                comida.getNombre(),
                comida.getTipo(),
                comida.getValor()
            });
        }
    }
